"""Core logic for ``opcli pytest expand`` and ``opcli pytest run``.

Reads ``artifacts.build.yaml`` (schema v1) and assembles the flags that
tox/pytest need to locate built charms and their OCI-image resources.

Convention (matching operator-workflows):
    --charm-file=<path>             for each locally built charm
    --<rock-name>-image=<path>      for each rock (directly, regardless of charm links)
    --<resource-name>=<path>        for OCI-image resources not backed by a rock

Rock image flags are generated by iterating over ``rocks`` directly, so no
explicit ``rock:`` annotation in ``artifacts.yaml`` is required.  The
``rock:`` field is still used for publishing (knowing which rock backs which
charm resource), but it is not needed for test flag generation.

The ``KEY=VALUE`` form is required because some conftest.py files register
``--charm-file`` with ``nargs="+"``, which makes argparse greedy.  With the
space-separated form, subsequent ``--charm-file`` tokens would be consumed as
values of the first flag instead of starting a new flag.

When the generated file contains multi-arch builds, flags are filtered to the
current machine's architecture.  If no build matches the current arch, all
available builds are used (graceful degradation for single-arch repos).
"""

import logging
import platform
from pathlib import Path
from typing import overload

from opcli.core.env import is_ci
from opcli.core.exceptions import ConfigurationError
from opcli.core.secrets import load_secrets_env
from opcli.core.subprocess import run_command
from opcli.core.yaml_io import load_artifacts_build
from opcli.models.artifacts_build import (
    CharmOutput,
    RockOutput,
    SnapOutput,
)

logger = logging.getLogger(__name__)

_ARTIFACTS_GENERATED_YAML = "artifacts.build.yaml"


def pytest_run(
    root: Path,
    *,
    tox_env: str = "integration",
    extra_args: list[str] | None = None,
    ci: bool | None = None,
) -> None:
    """Assemble the tox command and execute it interactively.

    In local mode, secrets from ``.secrets.env`` (if present) are loaded and
    passed as environment variables to the tox subprocess.  In CI mode the
    variables are expected to already be in the environment.

    Raises:
        ConfigurationError: If ``artifacts.build.yaml`` is missing.
        SubprocessError: If tox exits non-zero.
    """
    cmd = assemble_tox_argv(root, tox_env=tox_env, extra_args=extra_args)

    is_ci_env = ci if ci is not None else is_ci()
    secrets_env: dict[str, str] | None = None
    if not is_ci_env:
        loaded = load_secrets_env(root)
        if loaded:
            secrets_env = loaded

    run_command(cmd, cwd=str(root), interactive=True, env=secrets_env)


def assemble_tox_argv(
    root: Path,
    *,
    tox_env: str = "integration",
    extra_args: list[str] | None = None,
) -> list[str]:
    """Build the full tox argv for running integration tests.

    Returns a list of tokens suitable for shell execution or display via
    ``shlex.join()``.  The ``--`` separator is included only when there are
    pytest flags or forwarded extra args to pass.

    Raises:
        ConfigurationError: If required YAML files are missing.
    """
    assembled = assemble_pytest_args(root)
    pytest_args = assembled + (extra_args or [])

    cmd: list[str] = ["tox", "-e", tox_env]
    if pytest_args:
        cmd += ["--", *pytest_args]
    return cmd


def assemble_pytest_args(  # noqa: C901
    root: Path,
) -> list[str]:
    """Build the list of pytest flags from the generated artifacts.

    Returns:
        A list of CLI flags like
        ``["--charm-file=path.charm", "--img=path.rock"]``.

    Raises:
        ConfigurationError: If ``artifacts.build.yaml`` is missing.
    """
    gen_path = root / _ARTIFACTS_GENERATED_YAML
    if not gen_path.exists():
        msg = f"{_ARTIFACTS_GENERATED_YAML} not found. Run 'opcli artifacts build' first."
        raise ConfigurationError(msg)

    generated = load_artifacts_build(gen_path)
    arch = _current_arch()

    args: list[str] = []

    # Rock image flags — one per rock, matching operator-workflows convention.
    # This runs unconditionally so no rock: annotation is needed on charm resources.
    rock_flag_names: set[str] = set()
    for rock in generated.rocks:
        rock_builds = _select_arch_builds(rock.builds, arch, rock.name)
        for rock_build in rock_builds:
            value = rock_build.image or rock_build.file
            if value:
                flag_name = f"{rock.name}-image"
                rock_flag_names.add(flag_name)
                args.append(f"--{flag_name}={value}")

    for charm in generated.charms:
        charm_builds = _select_arch_builds(charm.builds, arch, charm.name)
        for charm_build in charm_builds:
            if charm_build.path:
                args.append(f"--charm-file={charm_build.path}")
            elif charm_build.artifact:
                logger.warning(
                    "Skipping --charm-file for charm '%s' (%s): output is a CI "
                    "artifact (%s). Run 'opcli artifacts build' locally to get "
                    "a local file.",
                    charm.name,
                    charm_build.arch,
                    charm_build.artifact,
                )

        # Resources backed by a rock are already covered by the rock
        # iteration above. Resources without a rock link have no output info.
        for res_name, res in (charm.resources or {}).items():
            if res.rock:
                continue
            if res_name in rock_flag_names:
                logger.warning(
                    "Skipping resource '%s' for charm '%s': flag name '--%s' "
                    "already emitted by a rock with the same name.",
                    res_name,
                    charm.name,
                    res_name,
                )
                continue

    return args


